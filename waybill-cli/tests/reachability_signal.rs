/// SC-001 + SC-005 — a reachability tool's jq-substring check returns
/// `true` on a constraint-only pip scan. The exact substring
/// `"transitive-edges-unresolvable: "` (with trailing colon-space) is
/// the load-bearing wire signal.
#[test]
fn t001_us1_machine_check_true_on_design_tier_scan() {
    let tmp = tempfile::tempdir().expect("tempdir");
    write_pip_requirements_only(tmp.path());

    let (sbom, _stderr) = scan_cdx(tmp.path());

    let reason = graph_completeness_reason(&sbom).expect(
        "US1: mikebom:graph-completeness-reason MUST be present when the classifier fires",
    );
    assert!(
        reason.contains(CODE_SUBSTRING),
        "US1 SC-001: reason value MUST contain {CODE_SUBSTRING:?} substring; got {reason:?}"
    );
    assert!(
        reason.contains("transitive-edges-unresolvable: pypi"),
        "US1 SC-001: reason value MUST name the affected ecosystem (pypi); got {reason:?}"
    );
}

/// US2 acceptance scenario 1 — `graph-completeness` value flips from
