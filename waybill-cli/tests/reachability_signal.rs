/// FR-005 + SC-005 offline-orthogonality gate — the reason code MUST
/// fire under `--offline`. `scan_cdx` already passes `--offline`; this
/// test makes the FR-005 contract explicit and locks it in.
#[test]
fn t003_us2_reason_fires_under_offline() {
    let tmp = tempfile::tempdir().expect("tempdir");
    write_pip_requirements_only(tmp.path());

    // scan_cdx invokes with `--offline` unconditionally per helper design.
    let (sbom, _stderr) = scan_cdx(tmp.path());

    let reason = graph_completeness_reason(&sbom).expect(
        "FR-005: reason MUST fire under --offline (network state is orthogonal)",
    );
    assert!(
        reason.contains(CODE_SUBSTRING),
        "FR-005: --offline scan MUST still emit the m177 code; got {reason:?}"
    );
}

/// Edge case — empty scan target (zero manifests, zero components) →
/// the graph-completeness annotation itself isn't emitted; the reason
/// cannot appear standalone. Verifies the classifier doesn't crash or
/// mis-fire on the empty case.
#[test]
fn t004_us2_empty_scan_target_silent() {
    let tmp = tempfile::tempdir().expect("tempdir");
    std::fs::write(tmp.path().join("README.txt"), b"empty scan target\n")
        .expect("write README");

    let (sbom, _stderr) = scan_cdx(tmp.path());

    // On empty scans, either the annotation is absent OR (if it exists)
    // its value must NOT contain the m177 code (per Edge Cases in spec).
    match graph_completeness_reason(&sbom) {
        None => { /* annotation absent — acceptable */ }
        Some(reason) => {
            assert!(
                !reason.contains(CODE_SUBSTRING),
                "empty scan MUST NOT emit m177 code; got reason value {reason:?}"
            );
        }
    }
}

