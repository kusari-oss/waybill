/// US3 SC-003 — polyglot scan (cargo lockfile-resolved + pip
/// constraint-only) names ONLY pypi in the reason detail, NOT cargo.
/// Reachability tools can safely analyze the cargo subgraph while
/// filtering out pypi.
#[test]
fn t005_us3_polyglot_pypi_only_not_cargo() {
    let tmp = tempfile::tempdir().expect("tempdir");
    write_cargo_with_lockfile(&tmp.path().join("rust"));
    std::fs::write(
        tmp.path().join("requirements.txt"),
        "requests>=2.31.0\n",
    )
    .expect("write requirements.txt");

    let (sbom, _stderr) = scan_cdx(tmp.path());

    let reason = graph_completeness_reason(&sbom)
        .expect("US3: reason MUST be present on polyglot mixed-tier scan");
    assert!(
        reason.contains("transitive-edges-unresolvable: pypi"),
        "US3 SC-003: reason MUST name pypi (constraint-only ecosystem); got {reason:?}"
    );
    // Cargo is source-tier (lockfile-resolved), so MUST NOT appear in
    // the m177 code's detail.
    assert!(
        !reason.contains("transitive-edges-unresolvable: cargo"),
        "US3 SC-003: reason MUST NOT name cargo (source-tier-resolved); got {reason:?}"
    );
    assert!(
        !reason.contains("transitive-edges-unresolvable: cargo, pypi"),
        "US3 SC-003: reason MUST NOT include cargo in the m177 code's ecosystem list; got {reason:?}"
    );
}

